using System;
using System.Collections.Generic;
using System.Globalization;
using System.Linq;
using System.Text;
using System.Threading.Tasks;
using System.Windows.Data;
using System.Windows;

namespace QuanLyChuoiCuaHangCoffee.Utils.ConverterValue
{
    public class StatusVoucherConverter : IValueConverter
    {
        // This converts the result object to the foreground.
        public object Convert(object value, Type targetType,
            object parameter, CultureInfo language)
        {
            // Retrieve the format string and use it to format the value.
            string text = value as string;

            if (text == Utils.VOUCHER_STATUS.RELEASED)
                return Visibility.Collapsed;
            else if (text == Utils.VOUCHER_STATUS.USED || text == Utils.VOUCHER_STATUS.UNRELEASED)
                return Visibility.Visible;
            else
                return Visibility.Collapsed;
        }

        public object ConvertBack(object value, Type targetType, object parameter, CultureInfo culture)
        {
            throw new NotImplementedException();
        }
    }
}
